public class Unit1Task0 {
    public static void main(String[] args) {
        int month = 10;
        int day = 18;
        System.out.println("month + day = " + (month+day));
        System.out.println("month - day = " + (month-day));
        System.out.println("month * day = " + (month*day));
        System.out.println("month / day = " + (month/day));
        System.out.println("month += day = " + (month+=day));   //month = month + day
        System.out.println("month -= day = " + (month-=day));   //month = month - day
        System.out.println("month *= day = " + (month*=day));   //month = month * day
        System.out.println("month /= day = " + (month/=day));   //month = month / day
        System.out.println("month % day = " + (month%day));
        System.out.println("month %= day = " + (month%=day));   //month = month%day
        System.out.println("month++ = " + (month++));
        System.out.println("--day = " + (--day));
        System.out.println("month > day = " + (month > day));
        System.out.println("month < day = " + (month < day));
        System.out.println("month >= day = " + (month >= day));
        System.out.println("month <= day = " + (month <= day));
        System.out.println("month == day = " + (month == day));
        System.out.println("month != day = " + (month != day));
        System.out.println("month > day = " + (month > day));
        System.out.println("month || day = ");  //boolean type
        System.out.println("month && day = ");  //boolean type
        System.out.println("!month");   //boolean type
        System.out.println("month | day = " + (month | day));   //1010 | 10010 = 11011
        System.out.println("month & day = " + (month & day));   //1010 & 10010 = 1
        System.out.println("month ^ day = " + (month ^ day));   //1010 ^ 10010 = 11010
        System.out.println("~day = " + (~day));     //~10010 = ?


    }
}
